  TABLESPACE RDM_DATA01
  ENABLE       STORAGE IN ROW
  CHUNK       16384
  RETENTION
  NOCACHE
  LOGGING)
LOGGING 
NOCOMPRESS 
NOCACHE
NOPARALLEL
MONITORING;
